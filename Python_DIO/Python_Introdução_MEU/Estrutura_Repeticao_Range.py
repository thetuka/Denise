# range(stop) -> range object
# range(start, stop[, step]) -> range object

#range com for
for numero in range(0, 11):
    print(numero, end=" ")
    
for numero in range(0, 51, 5):
    print(numero, end=" ") #se colocar o end=" ", ele coloca os itens um ao lado do outro. Sem o end=" ", os numeros ficariam um abaixo do outro
    